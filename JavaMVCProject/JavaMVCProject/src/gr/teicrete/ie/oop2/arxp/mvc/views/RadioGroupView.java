    @Override
    public void selectionChangedAction(int index) {
       ((JRadioButton)getContentPane().getComponent(index)).setSelected(true);
    }

    @Override
    public void itemStateChanged(ItemEvent ie) {
        if( ((JRadioButton)ie.getItemSelectable()).isSelected() ){
            int selectedIndex;
            for (int i = 0; i < getContentPane().getComponentCount(); i++) {
                if(((JRadioButton)getContentPane().getComponent(i))== (JRadioButton)ie.getItemSelectable()){
                    selectedIndex = i;
                    singleSelectionController.setSelectedIndex(this, selectedIndex);
                    break;
                }
            }
        }
    }

