    private SingleSelectionController singleSelectionController;
    private ButtonGroup group ;

    /**
     * Creates new form RadioGroupView
     */
    public RadioGroupView(SingleSelectionController singleSelectionController) {
        this.singleSelectionController = singleSelectionController;
        initComponents();
        //Group the radio buttons.
        group = new ButtonGroup();
        for (int i = 0; i < singleSelectionController.getInitialModelValues().length; i++) {
            JRadioButton jRadioButton = new JRadioButton(singleSelectionController.getInitialModelValues()[i]);
            group.add(jRadioButton);
            jRadioButton.setSelected(singleSelectionController.getSelectedIndex() == i);
            
            jRadioButton.addItemListener(this);
                        
            add(jRadioButton);
        }
//        ((JRadioButton)getContentPane().getComponent(singleSelectionController.getSelectedIndex()-1)).setSelected(true);
        
        ((GridLayout) getContentPane().getLayout()).setRows(singleSelectionController.getInitialModelValues().length);
        pack();
    }

